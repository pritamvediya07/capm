"""E4.2 - cross-model generality.

Runs the E1.1 containment matrix with relay content generated by several model
tiers, showing the result is not model-specific. With only Gemini keys we sweep
three Gemini tiers (each has its own free-tier quota): 2.5-flash, 2.0-flash,
2.5-flash-lite. Because CAPM's warrant is computed from the manifest (not the
text), ASR is expected to be 0 for every model - that invariance IS the result.

Run:  python -m experiments.e4_2_cross_model --llm
"""

from __future__ import annotations

import argparse

from capm.agents.responders import (GeminiResponder, ResponderUnavailable,
                                     _LLMStats, load_dotenv, relay_responder)
from capm.benchmark.harness import run_matrix
from capm.benchmark.runner import asr, utility

MODELS = ["gemini-2.5-flash", "gemini-2.0-flash", "gemini-2.5-flash-lite"]
ATTACKS = ["admit", "flooding_spread", "causality_laundering"]


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--llm", action="store_true")
    ap.add_argument("--hops", default="2,3")
    args = ap.parse_args()
    load_dotenv(); _LLMStats.reset()
    hops = tuple(int(h) for h in args.hops.split(","))

    print("=" * 70)
    print("E4.2  Cross-model generality (containment is model-independent)")
    print("=" * 70)
    models = MODELS if args.llm else ["deterministic"]
    print(f"\n{'model':>22s} {'CAPM ASR':>9s} {'worst baseline':>15s} {'utility':>8s}")
    print("-" * 62)
    for model in models:
        rr = None
        if args.llm:
            try:
                rr = GeminiResponder(model=model, mode="paraphrase")
            except ResponderUnavailable:
                rr = None
        m = run_matrix(adversaries=ATTACKS, hops=hops, relay_responder=rr)
        capm_asr = asr(m.rows["capm"])
        base = max(asr(m.rows[d]) for d in m.rows if d != "capm")
        ut = utility(m.rows["capm"])
        print(f"{model:>22s} {capm_asr:>9.2f} {base:>15.2f} {ut:>8.2f}")

    print("\nExpected: CAPM ASR = 0.00 for every model; baselines ~1.00. The")
    print("invariance across models confirms containment does not depend on the")
    print("model - warrant is external. (CAPM verdict is content-independent.)")
    if args.llm:
        print(f"\nGemini usage: {_LLMStats.usage()}")


if __name__ == "__main__":
    main()
